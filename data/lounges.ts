/** Return lounges for an airport, optionally filtered by a search string. */
export function getLoungesForAirport(airportCode: string, query: string): string[] {
  if (!query.trim()) return [];
  const all = AIRPORT_LOUNGES[airportCode.toUpperCase()] ?? [];
  const q = query.toLowerCase();
  return all.filter(name => name.toLowerCase().includes(q)).slice(0, 6);
}
